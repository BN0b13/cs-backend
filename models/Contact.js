const mongoose = require('mongoose');


const ContactSchema = mongoose.Schema({
  firstName: {
    type: String,
    required: true
  },
  lastName: {
    type: String,
    required: true
  },
  phone: {
    type: String,
    required: true
  },
  email: {
    type: String,
    required: true
  },
  message: {
    type: String,
    required: true
  },
  date: {
    type: Date,
    default: Date.now
  }
});



// first parameter is folder name in DB Collection, second is const/function called
module.exports = mongoose.model('contact', ContactSchema);